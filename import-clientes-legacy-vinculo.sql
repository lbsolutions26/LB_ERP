-- Vinculo de cliente legacy para cliente atual
-- Atualiza documentos_venda.cliente_id para exibir nome do cliente

create table if not exists public.clientes_legacy_map (
  id bigint generated by default as identity primary key,
  empresa_id uuid not null references public.empresas(id),
  legacy_id text not null,
  nome_legacy text,
  cliente_id bigint references public.clientes(id),
  match_count integer not null default 0,
  match_status text not null default 'nao_encontrado',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (empresa_id, legacy_id)
);

alter table public.clientes_legacy_map enable row level security;

drop policy if exists clientes_legacy_map_tenant on public.clientes_legacy_map;
create policy clientes_legacy_map_tenant
on public.clientes_legacy_map
for all
using (public.user_belongs_to_empresa(empresa_id))
with check (public.user_belongs_to_empresa(empresa_id));

