  empresa_id, legacy_id, nome_legacy, cliente_id, match_count, match_status, now()
from m
on conflict (empresa_id, legacy_id)
do update set
  nome_legacy = excluded.nome_legacy,
  cliente_id = excluded.cliente_id,
  match_count = excluded.match_count,
  match_status = excluded.match_status,
  updated_at = now();

update public.documentos_venda d
set cliente_id = m.cliente_id,
    updated_at = now()
from public.clientes_legacy_map m
where d.empresa_id = m.empresa_id
  and d.cliente_legacy_id = m.legacy_id
  and m.match_status = 'vinculado'
  and m.cliente_id is not null
  and d.cliente_id is distinct from m.cliente_id;

