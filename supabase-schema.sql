-- GuPedal SaaS multiempresa
-- Todas as tabelas de negocio possuem empresa_id.

create extension if not exists pgcrypto;

create table if not exists public.empresas (
  id uuid primary key default gen_random_uuid(),
  nome text not null,
  created_at timestamp with time zone not null default now()
);

create table if not exists public.usuarios_empresas (
  user_id uuid not null references auth.users(id) on delete cascade,
  empresa_id uuid not null references public.empresas(id) on delete cascade,
  role text not null default 'owner',
  ativo boolean not null default true,
  created_at timestamp with time zone not null default now(),
  primary key (user_id, empresa_id)
);

create or replace function public.user_belongs_to_empresa(target_empresa_id uuid)
returns boolean
language sql
stable
as $$
  select exists (
    select 1
    from public.usuarios_empresas ue
    where ue.user_id = auth.uid()
      and ue.empresa_id = target_empresa_id
      and ue.ativo = true
  );
$$;

create or replace function public.current_empresa_id()
returns uuid
language sql
stable
as $$
  select ue.empresa_id
  from public.usuarios_empresas ue
  where ue.user_id = auth.uid()
    and ue.ativo = true
  order by ue.created_at asc
  limit 1;
$$;

create table if not exists public.clientes (
  id bigint generated by default as identity primary key,
  empresa_id uuid not null default public.current_empresa_id() references public.empresas(id),
  nome text not null,
  telefone text,
  email text,
  created_at timestamp with time zone not null default now()
);

create table if not exists public.produtos (
  id bigint generated by default as identity primary key,
  empresa_id uuid not null default public.current_empresa_id() references public.empresas(id),
  nome text not null,
  preco numeric(12,2) not null default 0,
  estoque integer not null default 0,
  ponto_pedido integer not null default 0,
  created_at timestamp with time zone not null default now()
);

create table if not exists public.pedidos (
  id bigint generated by default as identity primary key,
  empresa_id uuid not null default public.current_empresa_id() references public.empresas(id),
  cliente_id bigint references public.clientes(id),
  data_pedido timestamp with time zone not null default now(),
  status text not null default 'aberto',
  descricao text,
  valor_total numeric(12,2) not null default 0,
  created_at timestamp with time zone not null default now()
);

create table if not exists public.pedido_itens (
  id bigint generated by default as identity primary key,
  empresa_id uuid not null default public.current_empresa_id() references public.empresas(id),
  pedido_id bigint not null references public.pedidos(id) on delete cascade,
  produto_id bigint not null references public.produtos(id),
  quantidade integer not null check (quantidade > 0),
  valor_unitario numeric(12,2) not null check (valor_unitario >= 0),
  desconto numeric(12,2) not null default 0 check (desconto >= 0),
  total numeric(12,2) not null default 0,
  created_at timestamp with time zone not null default now()
);

create table if not exists public.orcamentos (
  id bigint generated by default as identity primary key,
  empresa_id uuid not null default public.current_empresa_id() references public.empresas(id),
  cliente_id bigint references public.clientes(id),
  data_orcamento timestamp with time zone not null default now(),
  status text not null default 'aberto',
  descricao text,
  valor_total numeric(12,2) not null default 0,
  created_at timestamp with time zone not null default now()
);

create table if not exists public.orcamento_itens (
  id bigint generated by default as identity primary key,
  empresa_id uuid not null default public.current_empresa_id() references public.empresas(id),
  orcamento_id bigint not null references public.orcamentos(id) on delete cascade,
  produto_id bigint not null references public.produtos(id),
  quantidade integer not null check (quantidade > 0),
  valor_unitario numeric(12,2) not null check (valor_unitario >= 0),
  desconto numeric(12,2) not null default 0 check (desconto >= 0),
  total numeric(12,2) not null default 0,
  created_at timestamp with time zone not null default now()
);

create table if not exists public.despesas (
  id bigint generated by default as identity primary key,
  empresa_id uuid not null default public.current_empresa_id() references public.empresas(id),
  data_despesa timestamp with time zone not null default now(),
  descricao text not null,
  categoria text,
  status text not null default 'aberto',
  valor numeric(12,2) not null check (valor >= 0),
  created_at timestamp with time zone not null default now()
);

