  nota_entrada_id bigint references public.notas_entrada(id) on delete set null,
  fornecedor_id bigint references public.fornecedores(id) on delete set null,
  origem text not null default 'nota_entrada',
  numero_titulo text,
  emissao timestamptz not null default now(),
  valor_original numeric(14,2) not null default 0,
  valor_aberto numeric(14,2) not null default 0,
