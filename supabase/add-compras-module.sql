-- Modulo de Compras: fornecedores, notas de entrada, itens e contas a pagar.

-- ========== FORNECEDORES ==========
create table if not exists public.fornecedores (
  id bigint generated by default as identity primary key,
  empresa_id uuid not null references public.empresas(id) on delete cascade,
  nome text not null,
  documento text,
  telefone text,
  email text,
  endereco text,
  cidade text,
  uf text,
  observacoes text,
  ativo boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists idx_fornecedores_empresa_nome
  on public.fornecedores (empresa_id, nome);

create unique index if not exists ux_fornecedores_empresa_documento
  on public.fornecedores (empresa_id, documento)
  where documento is not null and btrim(documento) <> '';

alter table public.fornecedores enable row level security;

drop policy if exists fornecedores_tenant on public.fornecedores;
create policy fornecedores_tenant on public.fornecedores
  for all using (
    public.user_belongs_to_empresa(empresa_id) or public.is_platform_admin()
  ) with check (
    public.user_belongs_to_empresa(empresa_id) or public.is_platform_admin()
  );

drop policy if exists fornecedores_platform_admin on public.fornecedores;
create policy fornecedores_platform_admin on public.fornecedores
  for all using (public.is_platform_admin()) with check (public.is_platform_admin());

grant select, insert, update, delete on public.fornecedores to authenticated;
grant usage, select on sequence public.fornecedores_id_seq to authenticated;

-- ========== NOTAS DE ENTRADA ==========
create table if not exists public.notas_entrada (
  id bigint generated by default as identity primary key,
  empresa_id uuid not null references public.empresas(id) on delete cascade,
  fornecedor_id bigint references public.fornecedores(id) on delete set null,
  numero_nf text,
  serie text,
  chave_acesso text,
  data_emissao date,
  data_entrada date not null default current_date,
  status text not null default 'rascunho',
  valor_produtos numeric(14,2) not null default 0,
  valor_desconto numeric(14,2) not null default 0,
  valor_frete numeric(14,2) not null default 0,
  valor_outras numeric(14,2) not null default 0,
  valor_total numeric(14,2) not null default 0,
  observacoes text,
  parcelas integer not null default 1,
  vencimento_primeira date,
  intervalo_dias integer not null default 30,
  forma_pagamento_id bigint references public.formas_pagamento(id) on delete set null,
  estoque_aplicado boolean not null default false,
  financeiro_aplicado boolean not null default false,
  raw_payload jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint notas_entrada_status_check check (
    status = any (array['rascunho'::text, 'lancada'::text, 'cancelada'::text])
  )
);

create index if not exists idx_notas_entrada_empresa_data
  on public.notas_entrada (empresa_id, data_entrada desc);

create index if not exists idx_notas_entrada_fornecedor
  on public.notas_entrada (empresa_id, fornecedor_id);

create index if not exists idx_notas_entrada_status
  on public.notas_entrada (empresa_id, status);

alter table public.notas_entrada enable row level security;

drop policy if exists notas_entrada_tenant on public.notas_entrada;
create policy notas_entrada_tenant on public.notas_entrada
  for all using (
    public.user_belongs_to_empresa(empresa_id) or public.is_platform_admin()
  ) with check (
    public.user_belongs_to_empresa(empresa_id) or public.is_platform_admin()
  );

drop policy if exists notas_entrada_platform_admin on public.notas_entrada;
create policy notas_entrada_platform_admin on public.notas_entrada
  for all using (public.is_platform_admin()) with check (public.is_platform_admin());

grant select, insert, update, delete on public.notas_entrada to authenticated;
grant usage, select on sequence public.notas_entrada_id_seq to authenticated;

