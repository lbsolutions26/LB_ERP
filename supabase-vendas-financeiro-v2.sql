create table if not exists public.contas_receber (
  id bigint generated by default as identity primary key,
  empresa_id uuid not null references public.empresas(id),
  documento_id bigint references public.documentos_venda(id) on delete set null,
  cliente_id bigint references public.clientes(id),
  origem text not null default 'venda',
  numero_titulo text,
  emissao timestamptz not null default now(),
  valor_original numeric(12,2) not null,
  valor_aberto numeric(12,2) not null,
  status text not null default 'aberto' check (status in ('aberto', 'parcial', 'recebido', 'cancelado')),
  observacoes text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.contas_receber_parcelas (
  id bigint generated by default as identity primary key,
  empresa_id uuid not null references public.empresas(id),
  conta_receber_id bigint not null references public.contas_receber(id) on delete cascade,
  numero_parcela integer not null,
  vencimento timestamptz,
  valor_parcela numeric(12,2) not null,
  valor_recebido numeric(12,2) not null default 0,
  status text not null default 'pendente' check (status in ('pendente', 'parcial', 'recebido', 'cancelado')),
  forma_pagamento_id bigint references public.formas_pagamento(id),
  observacoes text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (conta_receber_id, numero_parcela)
);

create table if not exists public.recebimentos (
  id bigint generated by default as identity primary key,
  empresa_id uuid not null references public.empresas(id),
  parcela_id bigint not null references public.contas_receber_parcelas(id) on delete cascade,
  data_recebimento timestamptz not null default now(),
  valor numeric(12,2) not null check (valor > 0),
  forma_pagamento_id bigint references public.formas_pagamento(id),
  conta_destino text,
  observacoes text,
  created_at timestamptz not null default now()
);

create index if not exists idx_recebimentos_data
  on public.recebimentos (empresa_id, data_recebimento desc);

-- Fluxo de caixa (entradas por recebimento + saidas por despesas pagas)
create or replace view public.vw_fluxo_caixa as
select
  r.empresa_id,
  r.data_recebimento as data_movimento,
  'entrada'::text as tipo_movimento,
  r.valor as valor,
  'recebimento'::text as origem,
  r.id as origem_id,
  cr.id as conta_receber_id,
  dv.id as documento_id,
  dv.tipo_documento,
  fp.nome as forma_pagamento,
  r.observacoes
from public.recebimentos r
join public.contas_receber_parcelas p on p.id = r.parcela_id
join public.contas_receber cr on cr.id = p.conta_receber_id
left join public.documentos_venda dv on dv.id = cr.documento_id
left join public.formas_pagamento fp on fp.id = coalesce(r.forma_pagamento_id, p.forma_pagamento_id)

union all

select
  d.empresa_id,
  d.data_despesa as data_movimento,
  'saida'::text as tipo_movimento,
  d.valor as valor,
  'despesa'::text as origem,
  d.id as origem_id,
  null::bigint as conta_receber_id,
  null::bigint as documento_id,
  null::text as tipo_documento,
  null::text as forma_pagamento,
  d.descricao as observacoes
from public.despesas d
where d.status = 'pago';

