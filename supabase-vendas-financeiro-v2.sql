create or replace function public.parse_datetime_br(value_text text)
returns timestamptz
language sql
immutable
as $$
  select case
    when value_text is null or btrim(value_text) = '' then null
    else to_timestamp(btrim(value_text), 'DD/MM/YYYY HH24:MI:SS')
  end;
$$;

create or replace function public.parse_numeric_br(value_text text)
returns numeric
language sql
immutable
as $$
  select case
    when value_text is null or btrim(value_text) = '' then null
    else nullif(
      replace(
        replace(
          replace(
            regexp_replace(value_text, '[^0-9,.-]', '', 'g'),
            '.',
            ''
          ),
          ',',
          '.'
        ),
        ' ',
        ''
      ),
      ''
    )::numeric
  end;
$$;

create or replace function public.parse_percent_numeric_br(value_text text)
returns numeric
language sql
immutable
as $$
  select case
    when value_text is null or btrim(value_text) = '' then null
    else nullif(
      replace(
        replace(regexp_replace(value_text, '[^0-9,.-]', '', 'g'), '.', ''),
        ',',
        '.'
      ),
      ''
    )::numeric
  end;
$$;

